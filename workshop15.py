if score >= 80 :
    print('Grade A')
elif score >= 70 :
    print('Grade B')
elif score >= 60 :
    print('Grade C')
elif score >= 50 :
    print('Grade D')
else : #อย่าลืม colon *** ตัวสุดท้ายสามารถใช้ else หรือ elif ก้ได้
    print('Grade F')
    print('อย่าเสียใจนะ')

